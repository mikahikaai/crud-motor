    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        if (cbPembeli.getSelectedItem().toString().isEmpty() || eIdMobil.getText().isEmpty() || eSisa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Field Belum Terisi Lengkap");
        } else {
            try {
                sql = "insert into pembelian values (?,?,?,?,?,?,?)";
                pst = conn.prepareStatement(sql);
                pst.setString(1, null);
                pst.setDate(2, new java.sql.Date(jDateChooser1.getDate().getTime()));
                pst.setString(3, cbJenis.getSelectedItem().toString());
                pst.setString(4, eIdPembeli.getText());
                pst.setString(5, eIdMobil.getText());
                pst.setBigDecimal(6, new BigDecimal(eBayar.getText().replaceAll("\\D+", "")));
                pst.setBigDecimal(7, new BigDecimal(eSisa.getText().replaceAll("\\D+", "")));
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                ulang();
            } catch (SQLException ex) {
                Logger.getLogger(MobilView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_bSimpanActionPerformed

