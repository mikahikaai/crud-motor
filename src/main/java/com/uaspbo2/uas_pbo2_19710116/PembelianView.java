    private void hitung() {
        int harga = Integer.valueOf(eHarga.getText().replaceAll("\\D+", ""));
        int bayar = Integer.valueOf(eBayar.getText().replaceAll("\\D+", ""));
        int sisa = harga - bayar;
        if (sisa < 0) {
            JOptionPane.showMessageDialog(null, "Jumlah Bayar Melebihi Ketentuan Harga");
            eBayarMouseClicked(null);
        } else {
            eSisa.setText(myFormatter.format(sisa));
            eBayar.setText(myFormatter.format(bayar));
        }
    }

