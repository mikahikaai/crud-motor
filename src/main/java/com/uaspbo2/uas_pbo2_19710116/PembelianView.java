    private void tPembelianMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tPembelianMouseClicked
        // TODO add your handling code here:
        cbJenis.setEnabled(true);
        bUbah.setEnabled(true);
        bHapus.setEnabled(true);
        int baris = tPembelian.getSelectedRow();
        id = tPembelian.getValueAt(baris, 0).toString();
        if (!id.isEmpty()) {
            try {
                jDateChooser1.setDate(new SimpleDateFormat("dd-MM-yyyy").parse(tPembelian.getValueAt(baris, 1).toString()));
                cbJenis.setSelectedItem(tPembelian.getValueAt(baris, 2).toString());
                eIdPembeli.setText(tPembelian.getValueAt(baris, 3).toString());
                eIdMobil.setText(tPembelian.getValueAt(baris, 4).toString());
                eBayar.setText(tPembelian.getValueAt(baris, 5).toString());
                eSisa.setText(tPembelian.getValueAt(baris, 6).toString());
                bSimpan.setEnabled(false);
                bUbah.setEnabled(true);
                bHapus.setEnabled(true);
            } catch (ParseException ex) {
                Logger.getLogger(PembelianView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        sql = "select * from pembeli where id=?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, eIdPembeli.getText());
            rs = pst.executeQuery();
            while (rs.next()) {
                cbPembeli.setSelectedItem(rs.getString("nama_lengkap"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(PembelianView.class.getName()).log(Level.SEVERE, null, ex);
        }
        sql = "select * from mobil where id=?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, eIdMobil.getText());
            rs = pst.executeQuery();
            while (rs.next()) {
                eMerek.setText(rs.getString(3));
                eModel.setText(rs.getString(4));
                eWarna.setText(rs.getString(5));
                eTransmisi.setText(rs.getString(6));
                eHarga.setText(myFormatter.format(rs.getDouble(7)));
            }
        } catch (SQLException ex) {
            Logger.getLogger(PembelianView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tPembelianMouseClicked

