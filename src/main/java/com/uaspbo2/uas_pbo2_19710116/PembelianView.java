    private void tampilData() {
        try {
            String[] judul = {"ID", "Tanggal Pembelian", "Jenis Pembayaran", "ID Pembeli", "ID Mobil",
                "Jumlah Terbayar", "Sisa Bayar"};
            DefaultTableModel dtm = new DefaultTableModel(null, judul);
            tPembelian.setModel(dtm);
            tPembelian.getColumnModel().getColumn(0).setPreferredWidth(30);

            if (eCari.getText().isEmpty()) {
                sql = "select * from pembelian";
                //sql = "select p.*, m.* from pembelian p inner join mobil m on p.mobil_id = m.id";
                pst = conn.prepareStatement(sql);
            } else {
                sql = "select * from pembelian where id like '%" + eCari.getText() + "%'";
                pst = conn.prepareStatement(sql);
            }
            rs = pst.executeQuery();
            while (rs.next()) {
                DecimalFormat myFormatter = new DecimalFormat("#,###");
                DateFormat df = new SimpleDateFormat("dd-MM-yyyy");
                String str6 = myFormatter.format(rs.getDouble(6));
                String str7 = myFormatter.format(rs.getDouble(7));
                String str2 = df.format(rs.getDate(2));
                String[] data = {rs.getString(1), str2, rs.getString(3), rs.getString(4), rs.getString(5),
                    str6, str7};
                dtm.addRow(data);
            }
            jDateChooser1.setDate(new java.util.Date());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }

    private void data_pembeli() {
        try {
            cbPembeli.removeAllItems();
            cbPembeli.addItem("");
            sql = "SELECT nama_lengkap FROM pembeli";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                cbPembeli.addItem(rs.getString(1));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }

