    private void tMobilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tMobilMouseClicked
        int baris = tMobil.getSelectedRow();
        String id = tMobil.getValueAt(baris, 0).toString();
        if (!id.isEmpty()) {
            int sisa = Integer.valueOf(tMobil.getValueAt(baris, 6).toString().replaceAll("\\D+", ""))-Integer.valueOf(PembelianView.eBayar.getText().replaceAll("\\D+", ""));
            PembelianView.eIdMobil.setText(id);
            PembelianView.eMerek.setText(tMobil.getValueAt(baris, 2).toString());
            PembelianView.eModel.setText(tMobil.getValueAt(baris, 3).toString());
            PembelianView.eWarna.setText(tMobil.getValueAt(baris, 4).toString());
            PembelianView.eTransmisi.setText(tMobil.getValueAt(baris, 5).toString());
            PembelianView.eHarga.setText(tMobil.getValueAt(baris, 6).toString());
            PembelianView.eSisa.setText(myFormatter.format(sisa));
            PembelianView.cbJenis.setEnabled(true);
            dispose();
        }
    }//GEN-LAST:event_tMobilMouseClicked

