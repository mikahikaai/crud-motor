    private void tampilData() {
        try {
            String[] judul = {"ID", "No. Plat", "Merek", "Model", "Warna", "Transmisi", "Harga", "Status"};
            DefaultTableModel dtm = new DefaultTableModel(null, judul);
            tMobil.setModel(dtm);
            tMobil.getColumnModel().getColumn(0).setPreferredWidth(30);

            if (eCari.getText().isEmpty()) {
                sql = "select * from mobil";
                pst = conn.prepareStatement(sql);
            } else {
                sql = "select * from mobil where merek like '%" + eCari.getText() + "%' or model like '%" + eCari.getText() + "%'";
                pst = conn.prepareStatement(sql);
            }
            rs = pst.executeQuery();
            while (rs.next()) {
                String str = myFormatter.format(rs.getDouble(7));
                String[] data = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5),
                    rs.getString(6), str, rs.getString(8)};
                dtm.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }

