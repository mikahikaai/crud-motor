    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        if (ePlat.getText().isEmpty() || eMerek.getText().isEmpty() || eModel.getText().isEmpty()
                || eWarna.getText().isEmpty() || eHarga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Field Belum Terisi Lengkap");
        } else {
            try {
                sql = "insert into mobil values (?,?,?,?,?,?,?,?)";
                pst = conn.prepareStatement(sql);
                pst.setString(1, null);
                pst.setString(2, ePlat.getText());
                pst.setString(3, eMerek.getText());
                pst.setString(4, eModel.getText());
                pst.setString(5, eWarna.getText());
                pst.setString(6, cbTransmisi.getSelectedItem().toString());
                pst.setBigDecimal(7, new BigDecimal(eHarga.getText().replaceAll("\\D+", "")));
                pst.setString(8, cbStatus.getSelectedItem().toString());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                ulang();
            } catch (SQLException ex) {
                Logger.getLogger(MobilView.class
                        .getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_bSimpanActionPerformed

