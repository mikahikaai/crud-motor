    private void tMobilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tMobilMouseClicked
        int baris = tMobil.getSelectedRow();
        id = tMobil.getValueAt(baris, 0).toString();
        if (!id.isEmpty()) {
            ePlat.setText(tMobil.getValueAt(baris, 1).toString());
            eMerek.setText(tMobil.getValueAt(baris, 2).toString());
            eModel.setText(tMobil.getValueAt(baris, 3).toString());
            eWarna.setText(tMobil.getValueAt(baris, 4).toString());
            cbTransmisi.setSelectedItem(tMobil.getValueAt(baris, 5).toString());
            eHarga.setText(tMobil.getValueAt(baris, 6).toString());
            cbStatus.setSelectedItem(tMobil.getValueAt(baris, 7).toString());
            bSimpan.setEnabled(false);
            bUbah.setEnabled(true);
            bHapus.setEnabled(true);
        }
    }//GEN-LAST:event_tMobilMouseClicked

